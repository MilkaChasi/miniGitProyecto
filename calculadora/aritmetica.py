"""Módulo aritmética: contiene funciones para sumar y restar dos números."""


def suma(x, n):
    """
    Suma.

    Parámetros:
    x (int): Número entero a evaluar.
    n (int): Número entero a evaluar.
    """
    return x + n


def resta(x, n):
    """
    Resta.

    Parámetros:
    x (int): Número entero a evaluar.
    n (int): Número entero a evaluar.
    """
    return x - n
