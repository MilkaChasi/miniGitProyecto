"""Módulo operaciones: contiene funciones básicas de suma, resta, multiplicación y división."""


def multiplicaciones(x, n):
    """
    División.

    Parámetros:
    x (int): Número entero a evaluar.
    n (int): Número entero a evaluar.
    """
    return x * n


def division(x, n):
    """
    Multiplicación.

    Parámetros:
    x (int): Número entero a evaluar.
    n (int): Número entero a evaluar.
    """
    return x / n


def suma(x, n):
    """
    Suma.

    Parámetros:
    x (int): Número entero a evaluar.
    n (int): Número entero a evaluar.
    """
    return x + n


def resta(x, n):
    """
    Resta.

    Parámetros:
    x (int): Número entero a evaluar.
    n (int): Número entero a evaluar.
    """
    return x - n
